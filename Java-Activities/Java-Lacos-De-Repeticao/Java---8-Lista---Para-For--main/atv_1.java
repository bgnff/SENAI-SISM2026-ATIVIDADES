package Atividades.Java_8_Lista_For;

public class atv_1 {
    public static void main(String[] args) throws InterruptedException {

        System.out.println("Iniciando contagem de 0 a 10...");
        Thread.sleep(1500);

        for (int n = 1; n <= 10; n++) {

            System.out.println(n);

        }
    }
}
